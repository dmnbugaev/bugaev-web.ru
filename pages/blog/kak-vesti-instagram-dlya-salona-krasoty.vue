                <li>Посты в ленту: 3–4 раза в неделю</li>
                <li>Сторис: ежедневно или через день (5–10 сторис)</li>
                <li>Рилсы: 1–2 раза в неделю (дают органический охват за счёт рекомендаций)</li>
