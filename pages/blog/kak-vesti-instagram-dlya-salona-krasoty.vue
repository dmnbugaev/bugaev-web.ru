                <li>Настройте быстрые ответы (шаблоны) на частые вопросы: цены, адрес, как записаться</li>
                <li>Для салонов с потоком 20+ сообщений в день — подключите <NuxtLink to="/telegram-bot-zapis">Telegram-бот</NuxtLink>, который автоматически обрабатывает запросы на запись</li>
