                <li><strong>Игнорирование директа.</strong> Каждый неотвеченный вопрос — потерянный клиент.</li>
                <li><strong>Личные фото вместо рабочих.</strong> Подписчики следят за салоном, не за личной жизнью мастера. Личный бренд — отдельная история и отдельная стратегия.</li>
